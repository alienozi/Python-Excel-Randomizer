workingsheet = leexcel.get_sheet_by_name("Sonuclar")

print(data)
print(homework)

p= 2

for c1, r1 in zip(data, homework):                                         #prints results
        r1 = random.choice(homework)
        print("{}".format(c1), "{}".format(r1))
        workingsheet.cell(row=p , column= 4).value = r1
        p = p+1

        continue

