import openpyxl
import random

leexcel = openpyxl.load_workbook('test.xlsx')                                 #opens the file test.xlsx

mainsheet = leexcel.get_sheet_by_name("Veriler")                              #sets Veriler as mainsheet

studentnum = int(input("Öğrenci hücresi nerede bitiyor (ör: C4  ise 4)? "))   #gets student number and prints 
print ("Öğrenci Sayısı", studentnum, "olarak ayarlandı")
